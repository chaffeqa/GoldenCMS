$(function() {
    ////////////////////////////////////////////////////////////////////////////
	// Profile tab controls
	$('ul.folder-tabs li.fclick').click(function() {
		var id = this.id;
		$(this).addClass('active');
		$(this).siblings().removeClass('active')
		var content = $('.folder #body #' + id + '.main').show();
		content.siblings('.main').hide()
	    });
	$('ul.folder-tabs li:nth-child(2)').click();
    ////////////////////////////////////////////////////////////////////////////




    ////////////////////////////////////////////////////////////////////////////
    // Colorbox Operator
    $('a.gallery').colorbox();
    ////////////////////////////////////////////////////////////////////////////

});
